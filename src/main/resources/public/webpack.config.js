let webpack = require('webpack');
let CircularDependencyPlugin = require('circular-dependency-plugin');
let WebpackShellPlugin = require('webpack-shell-plugin');
let HtmlWebpackPlugin = require('html-webpack-plugin');

module.exports = {
    entry: "./ts/index.tsx",
    output: {
        filename: "bundle.js",

        //not needed with html-loader copying HTML files into 'dist'
        path: __dirname + "/dist"

        //publicPath: __dirname + "/dist"
    },

    // Enable sourcemaps for debugging webpack's output.
    devtool: "source-map",

    resolve: {
        // Add '.ts' and '.tsx' as resolvable extensions.
        extensions: [".ts", ".tsx", ".js", ".json"]
    },

    module: {
        rules: [
            // All files with a '.ts' or '.tsx' extension will be handled by 'awesome-typescript-loader'.
            {
                test: /\.tsx?$/,
                loader: "awesome-typescript-loader?configFileName=./tsconfig.js‌​on"
            },

            // All output '.js' files will have any sourcemaps re-processed by 'source-map-loader'.
            {
                enforce: "pre",
                test: /\.js$/,
                loader: "source-map-loader"
            },

            {
                test: /\.htm$/,
                use: ['html-loader']
            }
        ]
    },

    plugins: [
        new webpack.DefinePlugin({
            //this was a test: Didn't work. I tried it in the index.html and it was not translated.
            //I'll come back to this later.
            __VERSION__: JSON.stringify("180107")
        }),
        new WebpackShellPlugin({
            onBuildStart: ["./on-build-start.sh"],
            //onBuildEnd: ['whatever else']
        }),
        new CircularDependencyPlugin({
            // `onDetected` is called for each module that is cyclical
            onDetected({ module: webpackModuleRecord, paths, compilation }) {
                // `paths` will be an Array of the relative module paths that make up the cycle
                // `module` will be the module record generated by webpack that caused the cycle
                compilation.errors.push(new Error(paths.join(' -> ')))
            }
        }),
        new HtmlWebpackPlugin({
            template: './html/index.html',
            filename: 'index.html',
            hash: true
        })
    ]
};
